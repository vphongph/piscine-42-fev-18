void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	char *error;

	error = "error";
	if (!str)
	{
		while (*error)
			ft_putchar(*error++);
	}
	else
		while (*str)
			ft_putchar(*str++);
}
