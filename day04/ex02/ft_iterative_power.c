int	ft_iterative_power(int nb, int power)
{
	int i;

	i = nb;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	if (power == 1)
		return (nb);
	if (nb == 1)
		return (1);
	if (nb == 0 && power > 0)
		return (0);
	while (power > 1)
	{
		nb = nb * i;
		power--;
	}
	return (nb);
}
