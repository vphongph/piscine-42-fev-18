int	ft_atoi(char *str)
{
	int s;
	int nb;

	s = 0;
	nb = 0;
	while ((*str > 8 && *str < 14) || *str == 32 || *str == '0')
		str++;
	if ((s = *str == '-' ? -1 : 1) == -1 || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
		nb = nb * 10 + (*str++ - '0');
	return (nb * s);
}
