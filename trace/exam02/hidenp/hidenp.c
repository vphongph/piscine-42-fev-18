#include <unistd.h>

int     main(int ac, char **av)
{
	if (ac != 3)
	{
		write(1, "\n", 1);
		return (0);
	}

	int i = -1;
	int j = 0;
	
	if (!av[1][j])
	{
		write(1, "1\n", 2);
		return (0);
	}

	while (av[2][++i])
	{
		if (av[2][i] == av[1][j])
				j++;
		if (!av[1][j])
		{
			write(1, "1\n", 2);
			return (0);
		}
	}

	write(1, "0\n", 2);

	return (0);
}
