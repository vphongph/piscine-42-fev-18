void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	while (*str)
		ft_putchar(*str++);
}

int		main(int ac, char **av)
{
	if (ac != 1)
		return (0);
	ft_putstr(av[0]);
	ft_putchar('\n');
	return (0);
}
